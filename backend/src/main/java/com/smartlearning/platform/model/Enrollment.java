package com.smartlearning.platform.model;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
// HIGHLIGHT: Uncommented the import for the Course class
import com.smartlearning.platform.model.Course;
// HIGHLIGHT: Uncommented the import for the User class
import com.smartlearning.platform.model.User;
import jakarta.persistence.*;

@Entity
@Table(name = "enrollments")
@JsonIgnoreProperties({"hibernateLazyInitializer", "handler"})
public class Enrollment {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @ManyToOne(fetch = FetchType.LAZY)
    @JoinColumn(name = "user_id", nullable = false)
    private User user;

    @ManyToOne(fetch = FetchType.LAZY)
    @JoinColumn(name = "course_id", nullable = false)
    private Course course;

    // Default constructor
    public Enrollment() {}

    // Getters and setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public Course getCourse() {
        return course;
    }

    public void setCourse(Course course) {
        this.course = course;
    }
}